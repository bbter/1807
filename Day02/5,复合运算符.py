'''
复合运算符
+=  a += b.txt     a = a + b.txt
-=  a -= b.txt     a = a - b.txt
*=  a *= b.txt     a = a * b.txt
/=  a /= b.txt     a = a / b.txt
%=  a %= b.txt     a = a % b.txt
**= a **= b.txt    a = a ** b.txt
//= a //= b.txt    a = a // b.txt

'''

