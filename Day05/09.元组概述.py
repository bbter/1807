'''
元组概述
    创建元组
        tuple = (元素1，元素2，元素3)
        元组可以存放多种数据类型数据,可以嵌套,可以存放可变类型数据
        tuple01 = (1) 不是元组
        tuple01 = (1) 是元组
'''
list00 = [1, 2, 30]
print(type(list00))

list00 = (1, 2, 30)
print(type(list00))

list01 = [0]
print(type(list01))

tuple01 = (1)
print(type(list01))

tuple01 = (0,)
print(type(list01))

tuple02 = (123, "list", True, [1, 2, 3])
print(tuple02)

tuple03 = ((1,2,3),"hello")
print(tuple03)




















